package org.example;

public class operators
{
    public static void main(String[] args) {

        int a=2,b=3;

        //addition
        a += 5;
        System.out.println(a);

        //subtract
        a-=3;
        System.out.println(a);

        //multiply
        b*=4;
        System.out.println(b);
    }
}
